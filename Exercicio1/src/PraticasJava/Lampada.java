package PraticasJava;

public class Lampada extends MarcaDeLampada{
private String tipoDeLampada;
private String voltagem;
private String cor;
public String getTipoDeLampada() {
	return tipoDeLampada;
}
public void setTipoDeLampada(String tipoDeLampada) {
	this.tipoDeLampada = tipoDeLampada;
}
public String getVoltagem() {
	return voltagem;
}
public void setVoltagem(String string) {
	this.voltagem = string;
}
public String getCor() {
	return cor;
}
public void setCor(String cor) {
	this.cor = cor;
}
}
